# print() 的用法
# (1) 輸出一個東西
print('output thing1')

# (2) 輸出多個東西，不同東西之間要用 , 隔開。效果會是在每個東西之間有一個空格
print('A','BBB',              'CC')

# 東西？？？ 資料 data 
# (1) 字。特色 "這是字"  '這是字'
print('這是字', "這也是字")

# (2) 數字
print(1, 3, -1, "這些是數字")

# 電腦擅長算數（超厲害計算機），計算機其實不懂語言的文字。
print(1+1)      # 輸出一個東西：1+1 的運算結果
print('1+1')    # 輸出一個東西：'1+1'

# 先存 data，再輸出

# 範例 1
fruit = 'banana'     # syntax 語法： 名稱 = data。
print(fruit)
# 變數(空間) fruit 存 'apple' 資料

# 範例 2
num = 10             # syntax 語法： 名稱 = data。 
print(num+20)
# 變數(空間) num 存 10 資料


# 隨堂測驗
print(1, '2', 3+4, '5+6', "七")

# 先預測，再對答案
# 1 2 7 5+6 七
